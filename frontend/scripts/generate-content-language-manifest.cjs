"use strict";

const fs = require("fs");
const path = require("path");

const frontendRoot = path.resolve(__dirname, "..");
const repoRoot = path.resolve(frontendRoot, "..");
const articlesDir = path.join(repoRoot, "articles");
const languagesFile = path.join(frontendRoot, "lib", "i18n", "languages.ts");
const outputTsFile = path.join(frontendRoot, "lib", "i18n", "generated-content-language-manifest.ts");
const outputJsonFile = path.join(frontendRoot, "lib", "i18n", "generated-content-language-manifest.json");

const ROUTE_FILE_MAP = [
	{
		file: "freelancejobs.json",
		routes: ["/extra-income/freelance-jobs", "/extra-income/freelance-jobs/*"],
		group: "extra-income",
	},
	{
		file: "budgetdata.json",
		routes: ["/extra-income/budget", "/extra-income/budget/*"],
		group: "extra-income",
	},
	{
		file: "remotejobs.json",
		routes: ["/extra-income/remote-online-jobs", "/extra-income/remote-online-jobs/*"],
		group: "extra-income",
	},
	{
		file: "moneymakingapps.json",
		routes: ["/extra-income/money-making-apps", "/extra-income/money-making-apps/*"],
		group: "extra-income",
	},
	{
		file: "breakingnews.json",
		routes: ["/breaking-news", "/breaking-news/*"],
	},
	{
		file: "startablogdata.json",
		routes: ["/start-a-blog", "/start-a-blog/*"],
	},
];

function extractSupportedLanguages() {
	const source = fs.readFileSync(languagesFile, "utf8");
	const match = source.match(/supportedLanguages\s*=\s*\[([\s\S]*?)\]\s*as const/);
	if (!match) {
		throw new Error("Could not parse supportedLanguages from languages.ts");
	}
	return Array.from(match[1].matchAll(/"([^"]+)"/g), (entry) => entry[1]);
}

function intersectLanguagesAcrossDocs(docs, supportedLanguages) {
	if (!Array.isArray(docs) || docs.length === 0) {
		return ["en"];
	}

	let shared = new Set(supportedLanguages);

	for (const doc of docs) {
		let docLanguages = [];
		if (doc && doc.languages && typeof doc.languages === "object") {
			docLanguages = Object.keys(doc.languages).filter((lang) =>
				supportedLanguages.includes(lang),
			);
		}
		if (!docLanguages.includes("en")) {
			docLanguages.unshift("en");
		}
		shared = new Set(docLanguages.filter((lang) => shared.has(lang)));
	}

	return supportedLanguages.filter((lang) => shared.has(lang));
}

function buildManifest() {
	const supportedLanguages = extractSupportedLanguages();
	const manifest = {};
	const groupedLangs = new Map();

	for (const entry of ROUTE_FILE_MAP) {
		const fullPath = path.join(articlesDir, entry.file);
		const docs = JSON.parse(fs.readFileSync(fullPath, "utf8"));
		const langs = intersectLanguagesAcrossDocs(docs, supportedLanguages);

		entry.routes.forEach((route) => {
			manifest[route] = langs;
		});

		if (entry.group) {
			const existing = groupedLangs.get(entry.group);
			if (!existing) {
				groupedLangs.set(entry.group, langs);
			} else {
				groupedLangs.set(
					entry.group,
					existing.filter((lang) => langs.includes(lang)),
				);
			}
		}
	}

	if (groupedLangs.has("extra-income")) {
		manifest["/extra-income"] = groupedLangs.get("extra-income");
	}

	return manifest;
}

function writeManifestFiles(manifest) {
	const tsBody = `// Auto-generated by scripts/generate-content-language-manifest.cjs\n` +
		`export const GENERATED_CONTENT_ROUTE_LANGUAGES = ${JSON.stringify(manifest, null, 2)} as const;\n`;

	fs.writeFileSync(outputTsFile, tsBody, "utf8");
	fs.writeFileSync(outputJsonFile, `${JSON.stringify(manifest, null, 2)}\n`, "utf8");
}

function main() {
	const manifest = buildManifest();
	writeManifestFiles(manifest);
	console.log("Generated content language manifest.");
}

main();
