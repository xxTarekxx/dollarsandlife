function getTranslations(lang: string): UITranslations {
	return TRANSLATIONS[lang] ?? TRANSLATIONS['en'];
}

// ─────────────────────────────────────────────────────────────────────────────
// EXPORTED GETTERS  (called from server layout.tsx)
// ─────────────────────────────────────────────────────────────────────────────

/** Returns navbar labels for the given language (instant — no API call). */
export async function getNavLabels(lang: string): Promise<NavLabels> {
	return getTranslations(lang).nav;
}

/** Returns footer labels for the given language (instant — no API call). */
export async function getFooterLabels(lang: string): Promise<FooterLabels> {
	return getTranslations(lang).footer;
}

/** Returns breadcrumb section names for the given language (instant — no API call). */
export async function getBreadcrumbLabels(lang: string): Promise<BreadcrumbLabels> {
	return getTranslations(lang).breadcrumb;
}
